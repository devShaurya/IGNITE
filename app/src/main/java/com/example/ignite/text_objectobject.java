package com.example.ignite;

public class text_objectobject {
    private String array;
    private String name;

    public text_objectobject(String array,String name){
        this.array = array;
        this.name = name;
    }
    public text_objectobject(){}

    public String getArray() {
        return array;
    }

    public void setArray(String array) {
        this.array = array;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
